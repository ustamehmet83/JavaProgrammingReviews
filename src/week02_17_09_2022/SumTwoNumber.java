package week02_17_09_2022;

public class SumTwoNumber {
    public static void main(String[] args) {

        int number1 = 10;
        int number2 = 20;
        int sum = number1 + number2;
        System.out.println(number1 + " + " + number2 + " = " + sum);

        int a = 30;
        String s = "30";
        String s1 = s + a;
        System.out.println("s1 = " + s1);

    }
}
